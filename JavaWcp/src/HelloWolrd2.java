
public class HelloWolrd2 {

	public static void main(String[] args) {
		/*
		宣言(変数の型 変数名;)
		※変数は宣言しただけでは空っぽ＝nullの状態なので
		=を使い、右側の値を左側に代入する。
		値の代入(変数=値;)
		宣言したての変数は通常nullなので、この変数に初めて値を入力する事を初期化、代入された値を初期値という。
		*/
		String name = "佐藤";
		int num = 551;
		double dec = 67.8;
		
//		代入
		System.out.println(name);
		System.out.println(num);
		System.out.println(dec);
	}

}
